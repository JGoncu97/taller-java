package tarea1;

public class EjercicioRelacionalModificado3 {
	 public static void main(String[] args) {

		    int x = 34;
		    int y = 89;

		    boolean menorQue = (x < y);
		    System.out.println("34<89: " + menorQue);

		    boolean mayorQue = (x > y);
		    System.out.println("34>89: " + mayorQue);

		    boolean menorigualQue = (x <= y);
		    System.out.println("34<=89: " + menorigualQue);

		    boolean mayorigualQue = (x >= y);
		    System.out.println("34>=89: " + mayorigualQue);

		    boolean igualQue = (x == y);
		    System.out.println("34==89: " + igualQue);

		    boolean diferenteQue = (x != y);
		    System.out.println("34!=89: " + diferenteQue);

		  }

}
