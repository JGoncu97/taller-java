package tarea1;

public class EjercicioRelacionalModificado2 {
	public static void main(String[] args) {

	    int x = 20;
	    int y = 12;

	    boolean menorQue = (x < y);
	    System.out.println("20<12: " + menorQue);

	    boolean mayorQue = (x > y);
	    System.out.println("20>12: " + mayorQue);

	    boolean menorigualQue = (x <= y);
	    System.out.println("20<=12: " + menorigualQue);

	    boolean mayorigualQue = (x >= y);
	    System.out.println("20>=12: " + mayorigualQue);

	    boolean igualQue = (x == y);
	    System.out.println("20==12: " + igualQue);

	    boolean diferenteQue = (x != y);
	    System.out.println("20!=12: " + diferenteQue);

	  }

}
